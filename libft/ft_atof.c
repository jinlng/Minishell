#include "libft.h"

double	ft_atof(const char *str)
{
	double	r[3];
	int		s;

	r[0] = 0.0;
	r[1] = 0.0;
	r[2] = 1.0;
	s = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			s = -1;
	while (*str >= '0' && *str <= '9')
		r[0] = r[0] * 10.0 + (*str++ - '0');
	if (*str == '.' && str++)
	{
		while (*str >= '0' && *str <= '9')
		{
			r[1] = r[1] * 10.0 + (*str - '0');
			r[2] *= 10.0;
			str++;
		}
	}
	return (s * (r[0] + r[1] / r[2]));
}
